package com.fdmgroup.userregistration;

public class NoEntryPresentException extends Exception {

	private static final long serialVersionUID = -6708195076300593729L;

	public NoEntryPresentException() {
		super();
	}

	public NoEntryPresentException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoEntryPresentException(String message) {
		super(message);
	}

	public NoEntryPresentException(Throwable cause) {
		super(cause);
	}
	
	

}
