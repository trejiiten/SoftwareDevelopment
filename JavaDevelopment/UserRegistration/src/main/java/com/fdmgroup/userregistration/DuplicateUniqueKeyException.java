package com.fdmgroup.userregistration;

public class DuplicateUniqueKeyException extends Exception {

	private static final long serialVersionUID = 1429047186239088543L;

	public DuplicateUniqueKeyException() {
		super();
	}

	public DuplicateUniqueKeyException(String message, Throwable cause) {
		super(message, cause);
	}

	public DuplicateUniqueKeyException(String message) {
		super(message);
	}

	public DuplicateUniqueKeyException(Throwable cause) {
		super(cause);
	}
	
	

}
